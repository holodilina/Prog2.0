void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            // Console.Write($"{image[i, j]} ");
            if(image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");

        }
    Console.WriteLine();
    }
}

void FillImage(int row, int col)  // метод, который закрашивает картинку
{
    if(pic[row, col] == 0) // если пиксель не закрашен, т.е. равен 0
    {
        pic[row, col] = 1;  // если закрашен, то ставим 1
        FillImage(row-1, col); // вверх
        FillImage(row, col-1); // вниз
        FillImage(row+1, col); // влево
        FillImage(row, col+1); // вправо
    }
}

